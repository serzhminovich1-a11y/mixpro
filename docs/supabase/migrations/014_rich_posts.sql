-- Раньше content поста всегда был просто текстом. Теперь composer умеет
-- жирный/курсив/подчёркнутый/зачёркнутый + выбор шрифта и размера, и
-- сохраняет content уже как безопасный HTML (санитайзер на клиенте
-- пропускает только b/i/u/s/span+style/br/div/p — без script/onclick/ссылок).
-- Флаг is_rich нужен, чтобы старые посты (обычный текст) не пытались
-- распарситься как HTML и отрендерились как раньше.

alter table posts add column if not exists is_rich boolean not null default false;

comment on column posts.is_rich is
  'true — content это безопасный HTML из панели форматирования (санитайзится и на клиенте при сохранении, и при показе). false — content обычный текст (старые посты).';
