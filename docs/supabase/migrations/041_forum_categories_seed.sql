insert into forum_categories (title, description, order_index)
select v.title, v.description, v.order_index
from (values
  ('Сведение', 'Балансы, панорама, автоматизация, всё про то, как собрать микс', 0),
  ('Мастеринг', 'Финальная обработка, громкость, подготовка к релизу и площадкам', 1),
  ('Запись и вокал', 'Микрофоны, помещение, техника записи, обработка вокала', 2),
  ('Оборудование', 'Мониторы, интерфейсы, аутборд — обсуждение и советы (не продажа — для этого Барахолка)', 3),
  ('Плагины и DAW', 'Софт, плагины, настройка рабочего процесса', 4),
  ('Разбор треков', 'Скинь трек — сообщество послушает и даст обратную связь', 5),
  ('Общение', 'Всё, что не поместилось в темы выше', 6)
) as v(title, description, order_index)
where not exists (select 1 from forum_categories fc where fc.title = v.title);
